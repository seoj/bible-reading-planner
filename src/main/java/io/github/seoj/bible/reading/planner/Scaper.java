  public static void main(String[] args) throws Exception {
    List<Future<ArrayList<Verse>>> futures = new ArrayList<>();
    ExecutorService executorService = Executors.newFixedThreadPool(64);
    for (String link : LINKS) {
      futures.add(
          executorService.submit(
              () -> {
                Map<Verse, Verse> verses = new LinkedHashMap<>();
                Elements verseEls = Jsoup.parse(new URL(link), 30000).select("p span.text");
                verseEls.select("span.chapternum").remove();
                verseEls.select("sup").remove();
                for (Element verseEl : verseEls) {
                  Matcher matcher =
                      Pattern.compile("(\\w+)\\-(\\d+)\\-(\\d+)").matcher(verseEl.className());
                  matcher.find();
                  Verse verse =
                      new Verse(
                          matcher.group(1),
                          Integer.parseInt(matcher.group(2)),
                          Integer.parseInt(matcher.group(3)),
                          verseEl.text());
                  if (verses.containsKey(verse)) {
                    verses.computeIfPresent(verse, (k, v) -> v.merge(verse));
                  } else {
                    verses.put(verse, verse);
                  }
                }
                return new ArrayList<>(verses.values());
              }));
    }

    List<Verse> verses = new ArrayList<>();
    for (Future<ArrayList<Verse>> future : futures) {
      verses.addAll(future.get());
    }

    executorService.shutdown();
    executorService.awaitTermination(1, TimeUnit.DAYS);

    try (CSVWriter writer = new CSVWriter(new FileWriter("verses.csv"))) {
      for (Verse verse : verses) {
        writer.writeNext(
            new String[] {
              verse.book,
              String.valueOf(verse.chapterNumber),
              String.valueOf(verse.verseNumber),
              verse.text
            });
      }
    }
  }

  static class Verse {
    String book;
    int chapterNumber;
    int verseNumber;
    String text;

    Verse(String book, int chapterNumber, int verseNumber, String text) {
      this.book = book;
      this.chapterNumber = chapterNumber;
      this.verseNumber = verseNumber;
      this.text = text;
    }

    Verse merge(Verse other) {
      return new Verse(book, chapterNumber, verseNumber, this.text + " " + other.text);
    }

    @Override
    public int hashCode() {
      final int prime = 31;
      int result = 1;
      result = prime * result + ((book == null) ? 0 : book.hashCode());
      result = prime * result + chapterNumber;
      result = prime * result + verseNumber;
      return result;
    }

    @Override
    public boolean equals(Object obj) {
      if (this == obj) return true;
      if (obj == null) return false;
      if (getClass() != obj.getClass()) return false;
      Verse other = (Verse) obj;
      if (book == null) {
        if (other.book != null) return false;
      } else if (!book.equals(other.book)) return false;
      if (chapterNumber != other.chapterNumber) return false;
      if (verseNumber != other.verseNumber) return false;
      return true;
    }

    @Override
    public String toString() {
      return String.format("%s %s:%s %s", book, chapterNumber, verseNumber, text);
    }
  }
